package com.phocas.asset.rest.model;

import com.amazonaws.services.dynamodbv2.datamodeling.*;
import lombok.*;

@Data
@AllArgsConstructor
@NoArgsConstructor
@DynamoDBTable(tableName = "AssetEvent")
public class AssetEvent {
    @DynamoDBHashKey(attributeName = "id")
    @DynamoDBAutoGeneratedKey
    private String id;
    @DynamoDBIndexHashKey(globalSecondaryIndexNames = "asset")
//    @DynamoDBAttribute()
    private int asset;
//    @DynamoDBIndexRangeKey(globalSecondaryIndexNames = "trip")
    @DynamoDBAttribute(attributeName = "trip")
    private int trip;
    @DynamoDBAttribute(attributeName = "x")
    private double x;
    @DynamoDBAttribute(attributeName = "y")
    private double y;
    @DynamoDBAttribute(attributeName = "speed")
    private double speed;
//    @DynamoDBIndexRangeKey(globalSecondaryIndexNames = {"asset"},
//            attributeName = "created")
    @DynamoDBAttribute(attributeName = "created")
    private String created;
}
